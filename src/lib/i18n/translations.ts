export function getMonthName(month: number, language: Language = 'en'): string {
  return monthNames[language][month - 1];
}

export function getShortMonthName(month: number, language: Language = 'en'): string {
  return shortMonthNames[language][month - 1];
}

// Number formatting for Bangla
export function formatNumber(num: number, language: Language = 'en'): string {
  if (language === 'bn') {
    const banglaDigits = ['০', '১', '২', '৩', '৪', '৫', '৬', '৭', '৮', '৯'];
    return num.toString().replace(/\d/g, (digit) => banglaDigits[parseInt(digit)]);
  }
  return num.toString();
}
